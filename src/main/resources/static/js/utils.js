/**
 * 生成分页HTML
 */
function generatePagination(currentPage, totalPages, onPageChange) {
    if (totalPages <= 1) return '';

    let html = '<div class="pagination">';

    // 上一页
    if (currentPage > 1) {
        html += `<a href="javascript:;" class="page-btn" data-page="${currentPage - 1}">上一页</a>`;
    }

    // 页码
    const maxVisible = 5;
    let startPage = Math.max(1, currentPage - Math.floor(maxVisible / 2));
    let endPage = Math.min(totalPages, startPage + maxVisible - 1);

    if (endPage - startPage < maxVisible - 1) {
        startPage = Math.max(1, endPage - maxVisible + 1);
    }

    if (startPage > 1) {
        html += `<a href="javascript:;" class="page-btn" data-page="1">1</a>`;
        if (startPage > 2) {
            html += '<span class="page-ellipsis">...</span>';
        }
    }

    for (let i = startPage; i <= endPage; i++) {
        html += `<a href="javascript:;" class="page-btn ${i === currentPage ? 'active' : ''}" data-page="${i}">${i}</a>`;
    }

    if (endPage < totalPages) {
        if (endPage < totalPages - 1) {
            html += '<span class="page-ellipsis">...</span>';
        }
        html += `<a href="javascript:;" class="page-btn" data-page="${totalPages}">${totalPages}</a>`;
    }

    // 下一页
    if (currentPage < totalPages) {
        html += `<a href="javascript:;" class="page-btn" data-page="${currentPage + 1}">下一页</a>`;
    }

    html += `</div>`;
    return html;
}

